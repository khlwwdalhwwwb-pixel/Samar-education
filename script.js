const title = document.querySelector(".hero-content h1");

const text = title.innerText;

title.innerText = "";

let index = 0;

function typingEffect() {

    if (index < text.length) {

        title.innerHTML += text.charAt(index);

        index++;

        setTimeout(typingEffect, 70);

    }

}

